function downloadFile(filename, content, mime) {
  const blob = new Blob([content], {
    type: mime
  });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}
function toCsv(runs) {
  const headers = ["id", "course_id", "course_title", "status", "youtube_key_status", "total", "updated_videos", "updated_transcripts", "warnings_count", "failures_count", "warnings", "failures", "started_at", "finished_at"];
  const esc = (v) => {
    const s = v == null ? "" : typeof v === "string" ? v : JSON.stringify(v);
    return `"${s.replace(/"/g, '""')}"`;
  };
  const rows = runs.map((r) => [r.id, r.course_id, r.course_title ?? "", r.status, r.youtube_key_status, r.total, r.updated_videos, r.updated_transcripts, r.warnings?.length ?? 0, r.failures?.length ?? 0, (r.warnings ?? []).join(" | "), (r.failures ?? []).map((f) => `${f.lessonId}:${f.reason}`).join(" | "), r.started_at, r.finished_at ?? ""].map(esc).join(","));
  return [headers.join(","), ...rows].join("\n");
}
